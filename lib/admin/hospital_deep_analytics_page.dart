class _PremiumDoctorBarChart extends StatelessWidget {
  final Map<String, double> docRevenue;
  const _PremiumDoctorBarChart({required this.docRevenue});

  @override
  Widget build(BuildContext context) {
    final sorted = docRevenue.entries.toList()..sort((a, b) => b.value.compareTo(a.value));
    final display = sorted.take(5).toList();

    return Container(
      padding: const EdgeInsets.all(24),
      decoration: BoxDecoration(
        color: Colors.white,
        borderRadius: BorderRadius.circular(24),
        boxShadow: [BoxShadow(color: Colors.black.withOpacity(0.03), blurRadius: 15, offset: const Offset(0, 8))],
      ),
      child: Column(
        children: [
          SizedBox(
            height: 200,
            child: BarChart(
              BarChartData(
                gridData: FlGridData(show: true, drawVerticalLine: false, getDrawingHorizontalLine: (v) => FlLine(color: Colors.grey.shade100, strokeWidth: 1)),
                titlesData: FlTitlesData(
                  show: true,
                  rightTitles: AxisTitles(sideTitles: SideTitles(showTitles: false)),
                  topTitles: AxisTitles(sideTitles: SideTitles(showTitles: false)),
                  leftTitles: AxisTitles(
                    sideTitles: SideTitles(
                      showTitles: true,
                      reservedSize: 40,
                      getTitlesWidget: (v, m) => Text("\u20B9${(v/1000).toStringAsFixed(0)}k", style: const TextStyle(fontSize: 9, color: Colors.grey)),
                    ),
                  ),
                  bottomTitles: AxisTitles(
                    sideTitles: SideTitles(
                      showTitles: true,
                      getTitlesWidget: (v, m) {
                        if (v.toInt() < display.length) {
                          return Padding(
                            padding: const EdgeInsets.only(top: 8.0),
                            child: Text(display[v.toInt()].key.split(' ').last, style: const TextStyle(fontSize: 10, fontWeight: FontWeight.bold, color: Color(0xFF64748B))),
                          );
                        }
                        return const SizedBox();
                      },
                    ),
                  ),
                ),
                borderData: FlBorderData(show: false),
                barGroups: List.generate(display.length, (i) => BarChartGroupData(
                  x: i,
                  barRods: [
                    BarChartRodData(
                      toY: display[i].value,
                      gradient: const LinearGradient(colors: [Color(0xFF6366F1), Color(0xFFA855F7)], begin: Alignment.bottomCenter, end: Alignment.topCenter),
                      width: 20,
                      borderRadius: const BorderRadius.only(topLeft: Radius.circular(6), topRight: Radius.circular(6)),
                      backDrawRodData: BackgroundBarChartRodData(show: true, toY: (sorted.first.value == 0 ? 1000 : sorted.first.value) * 1.1, color: Colors.grey.shade50),
                    )
                  ],
                )),
              ),
            ),
          ),
          const SizedBox(height: 16),
          _legendItem(const Color(0xFF6366F1), "Doctor Revenue Breakdown (Top 5)"),
        ],
      ),
    );
  }

  Widget _legendItem(Color color, String label) {
    return Row(
      mainAxisAlignment: MainAxisAlignment.center,
      children: [
        Container(width: 8, height: 8, decoration: BoxDecoration(color: color, borderRadius: BorderRadius.circular(2))),
        const SizedBox(width: 8),
        Text(label, style: const TextStyle(fontSize: 10, color: Color(0xFF94A3B8), fontWeight: FontWeight.w600)),
      ],
    );
  }
}

