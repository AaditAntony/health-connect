          ),
        ],
      ),
    );
  }

  Widget _heroStat(String value, String label, IconData icon) {
    return Row(
      children: [
        Icon(icon, color: Colors.white38, size: 18),
        const SizedBox(width: 8),
        Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            Text(value, style: const TextStyle(color: Colors.white, fontWeight: FontWeight.bold, fontSize: 16)),
            Text(label, style: const TextStyle(color: Colors.white38, fontSize: 10)),
          ],
        ),
      ],
    );
  }

  Future<Map<String, dynamic>> _fetchAllHospitalData() async {
    double totalRevenue = 0;
    Map<String, double> docRevenue = {};
    Map<String, double> patientRevenue = {};
    Map<String, int> serviceMix = {'Scans': 0, 'Treatments': 0};
    List<Map<String, dynamic>> allRecords = [];

    final docSnapshot = await FirebaseFirestore.instance.collection('accounts')
        .where('role', isEqualTo: 'doctor')
        .where('hospitalId', isEqualTo: widget.hospitalId)
        .get();
    Map<String, String> doctorNames = {
      for (var doc in docSnapshot.docs) doc.id: (doc.data()['doctorName'] ?? 'Dr. Unknown')
    };

    final scanSnap = await FirebaseFirestore.instance.collection('scans')
        .where('hospitalId', isEqualTo: widget.hospitalId).get();
    for (var doc in scanSnap.docs) {
      final d = doc.data();
      final cost = (d['cost'] ?? 0.0);
      final timestamp = d['timestamp'] ?? d['createdAt'];
      if (timestamp != null) {
        totalRevenue += cost;
        docRevenue[d['doctorId'] ?? 'unknown'] = (docRevenue[d['doctorId']] ?? 0) + cost;
        patientRevenue[d['patientId'] ?? 'unknown'] = (patientRevenue[d['patientId']] ?? 0) + cost;
        serviceMix['Scans'] = (serviceMix['Scans'] ?? 0) + 1;
        allRecords.add({'timestamp': timestamp, 'cost': cost});
      }
    }

    final treatSnap = await FirebaseFirestore.instance.collection('treatments')
        .where('hospitalId', isEqualTo: widget.hospitalId).get();
    for (var doc in treatSnap.docs) {
      final d = doc.data();
      final cost = (d['cost'] ?? 0.0);
      final timestamp = d['timestamp'] ?? d['createdAt'];
      if (timestamp != null) {
        totalRevenue += cost;
        docRevenue[d['doctorId'] ?? 'unknown'] = (docRevenue[d['doctorId']] ?? 0) + cost;
        patientRevenue[d['patientId'] ?? 'unknown'] = (patientRevenue[d['patientId']] ?? 0) + cost;
        serviceMix['Treatments'] = (serviceMix['Treatments'] ?? 0) + 1;
        allRecords.add({'timestamp': timestamp, 'cost': cost});
      }
    }

    Map<String, double> docRevenueNamed = {};
    docRevenue.forEach((id, rev) {
      docRevenueNamed[doctorNames[id] ?? "Dr. Guest"] = rev;
    });

    return {
      'totalRevenue': totalRevenue,
      'docRevenue': docRevenueNamed,
      'patientRevenue': patientRevenue,
      'serviceMix': serviceMix,
      'totalRecords': scanSnap.docs.length + treatSnap.docs.length,
      'uniquePatients': patientRevenue.length,
      'allRecords': allRecords,
    };
  }
}

