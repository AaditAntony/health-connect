                      child: _GlassCard(
                        title: "SERVICE DISTRIBUTION",
                        subtitle: "Breakdown of consultation types.",
                        child: _ModernDonutChart(serviceMix: serviceMix),
                      ),
                    ),
                    const SizedBox(width: 16),
                    Expanded(
                      child: _GlassCard(
                        title: "TOP REVENUE SOURCES",
                        subtitle: "Highest paying patient accounts.",
                        child: _PatientImpactList(patientRevenue: patientRevenue),
                      ),
                    ),
                  ],
                ),
                
                const SizedBox(height: 28),
                _buildSectionHeader("FINANCIAL DENSITY TREND", "Historical visualization of total revenue flow from all clinical activities per transaction."),
                const SizedBox(height: 16),
                _PremiumRevenueAreaChart(allRecords: data['allRecords'] as List<Map<String, dynamic>>),
